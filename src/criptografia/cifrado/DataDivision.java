
package criptografia.cifrado;

public class DataDivision {

	private Integer	left;
	private Integer	top;
	private Integer	height;
	private Integer	width;
	private Boolean	ROI;


	public DataDivision() {

	}

	public Integer getLeft() {
		return this.left;
	}

	public void setLeft(final Integer left) {
		this.left = left;
	}

	public Integer getTop() {
		return this.top;
	}

	public void setTop(final Integer top) {
		this.top = top;
	}

	public Integer getHeight() {
		return this.height;
	}

	public void setHeight(final Integer height) {
		this.height = height;
	}

	public Integer getWidth() {
		return this.width;
	}

	public void setWidth(final Integer width) {
		this.width = width;
	}

	public Boolean getROI() {
		return this.ROI;
	}

	public void setROI(final Boolean rOI) {
		this.ROI = rOI;
	}

}
